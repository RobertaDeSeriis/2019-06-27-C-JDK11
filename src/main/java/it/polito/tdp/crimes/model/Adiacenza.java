package it.polito.tdp.crimes.model;

public class Adiacenza {
	
	String t;
	int p;
	
	
	public Adiacenza(String t, int p) {
		super();
		this.t = t;
		this.p = p;
	}


	public String getT() {
		return t;
	}


	public void setT(String t) {
		this.t = t;
	}


	public int getP() {
		return p;
	}


	public void setP(int p) {
		this.p = p;
	}
	
	
	
	
	
	
	
	
	

}
